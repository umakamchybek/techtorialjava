package loops.forLoop;

public class NestedPractice {
    public static void main(String[] args) {


        for(int i=0 ; i<3;  i++){

            System.out.println("I'm first for Loop");

            for(int j = 0; j<5; j++){
                System.out.println("I'm NESTED for Loop");
            }
        }











    }
}
